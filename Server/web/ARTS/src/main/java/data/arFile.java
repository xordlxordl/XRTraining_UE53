package data;

public class arFile
{
	private int nid;
	private String filename;	//아이디
    private int ver;
    
	public int getNid()
	{
		return nid;
	}
	public void setNid(int nid)
	{
		this.nid = nid;
	}
	
	public String getFilename()
	{
		return filename;
	}
	public void setFilename(String filename)
	{
		this.filename = filename;
	}
	
	public int getVer()
	{
		return ver;
	}
	public void setVer(int ver)
	{
		this.ver = ver;
	}
    
}
