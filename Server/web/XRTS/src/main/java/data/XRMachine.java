package data;

public class XRMachine
{
	private int nid;			//아이디
    private String name;		//이름
	private int uid;			//매칭된 유저 UID
	
	public int getNid()
	{
		return nid;
	}
	public void setNid(int nid)
	{
		this.nid = nid;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public int getUid()
	{
		return uid;
	}
	public void setUid(int uid)
	{
		this.uid = uid;
	}
	
	
	
}
